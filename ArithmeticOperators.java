class ArithmeticOperators {
    public static void main(String[] args) {
        byte a=100;
        byte b=50;
        byte add = (byte)(a+b);
        byte sub = (byte)(a-b);
        byte mul = (byte)(a*b);
        byte div = (byte)(a/b);
        byte mod = (byte)(a%b);
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(mod);
    }
}
